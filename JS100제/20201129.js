/*
[JS100제] 문제 16번
===================================================================
문장이 입력되면 거꾸로 출력하는 프로그램을 만들어 봅시다.
===================================================================
*/
const reverse = (str) => {
  const reverseStr = str.split("").reverse().join("");
  return reverseStr;
};
/*
  ===================================================================
  split() 메서드: 문자열을 배열로 반환
  reverse() 메서드: 배열의 순서를 반전
  join() 메서드: 원소를 모두 붙여 문자열로 반환
  ===================================================================
  */

